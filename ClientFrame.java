    private static String partsList() {
        com.webservices.OrderWebservice_Service service = new com.webservices.OrderWebservice_Service();
        com.webservices.OrderWebservice port = service.getOrderWebservicePort();
        return port.partsList();
    }

    private static int add(java.lang.String arg0, int arg1) {
        com.webservices.OrderWebservice_Service service = new com.webservices.OrderWebservice_Service();
        com.webservices.OrderWebservice port = service.getOrderWebservicePort();
        return port.add(arg0, arg1);
    }

    private static String checkOrder() {
        com.webservices.OrderWebservice_Service service = new com.webservices.OrderWebservice_Service();
        com.webservices.OrderWebservice port = service.getOrderWebservicePort();
        return port.checkOrder();
    }

    private static String check(int arg0) {
        com.webservices.OrderWebservice_Service service = new com.webservices.OrderWebservice_Service();
        com.webservices.OrderWebservice port = service.getOrderWebservicePort();
        return port.check(arg0);
    }



