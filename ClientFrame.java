    private void MessageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MessageActionPerformed

      ZMQ.Context context = ZMQ.context(1); // thread pool size 1
      ZMQ.Socket socket = context.socket(ZMQ.REQ);
      socket.connect("tcp://localhost:8890");
      // send message to socket
      String checkOrders = checkOrder();
      byte[] request = checkOrders.getBytes();
      socket.send(request, 0); // blocking by default
      // block for response
      byte[] response = socket.recv(0);
      String greeting = new String(response);
      System.out.println("ZeroMQGreetingClient received greeting "
         + greeting);
      socket.close();
      context.term();
      System.out.println("ZeroMQGreetingClient stopping");



        // TODO add your handling code here:
    }//GEN-LAST:event_MessageActionPerformed

